package com.infosys.ocj.utils;

public class RequestPager {
	private int  page;  //当前页码
	private int  rows;  //每页的行数。
	public int getPage() {
		return page;
	}
	public void setPage(int page) {
		this.page = page;
	}
	public int getRows() {
		return rows;
	}
	public void setRows(int rows) {
		this.rows = rows;
	}
	

}
